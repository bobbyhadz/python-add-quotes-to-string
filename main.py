# Add quotes to a string in Python

# ✅ Alternating single and double quotes
result_1 = '"apple"'

# -------------------------------------

# ✅ Add double quotes around a variable
my_str = 'hello world'

result = f'"{my_str}"'

print(result)  # 👉️ "hello world"

# -------------------------------------

# ✅ Add single quotes around a variable
my_str = "hello"

result = f"'{my_str}'"

print(result)  # 👉️ 'hello'

# -------------------------------------

# ✅ Escaping double quotes with a backslash
result_3 = "\"apple\""